package com.vtu.model;

public class DownWorks {
    private Long id;

    private Long worksId;

    private Long downTypes;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getWorksId() {
        return worksId;
    }

    public void setWorksId(Long worksId) {
        this.worksId = worksId;
    }

    public Long getDownTypes() {
        return downTypes;
    }

    public void setDownTypes(Long downTypes) {
        this.downTypes = downTypes;
    }
}